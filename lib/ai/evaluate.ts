// PST tables above are laid out rank 8 (top) to rank 1 (bottom), file a..h.
// For a White piece on (file f, rank r) [0-indexed], the table index is:
//   (7 - r) * 8 + f
// For Black we mirror vertically: r * 8 + f.
function pstValue(piece: PieceSymbol, color: Color, file: number, rank: number): number {
  const idx = color === "w" ? (7 - rank) * 8 + file : rank * 8 + file;
  return PST[piece][idx];
}

/** Evaluate the board from White's perspective (centipawns). */
export function evaluateBoard(game: Chess): number {
  const board = game.board(); // board()[0] is rank 8, board()[7] is rank 1
  let score = 0;
  for (let row = 0; row < 8; row++) {
    for (let col = 0; col < 8; col++) {
      const piece = board[row][col];
      if (!piece) continue;
      const rank = 7 - row; // board row 0 => rank 8 (index 7)
      const file = col;
      const base = PIECE_VALUE[piece.type];
      const pst = pstValue(piece.type, piece.color, file, rank);
      const contribution = base + pst;
      score += piece.color === "w" ? contribution : -contribution;
    }
  }
  return score;
}
